#Escribir un programa que pida dos números y muestre como resultado su división, cociente y resto

# Solicitar al usuario dos números
numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))

# Verificar si el segundo número es cero (para evitar división por cero)
if numero2 == 0:
    print("No se puede dividir por cero.")
else:
    # Calcular la división, cociente y resto
    division = numero1 / numero2
    cociente = numero1 // numero2
    resto = numero1 % numero2

    # Mostrar los resultados
    print(f"Resultado de la división: {division}")
    print(f"Cociente: {cociente}")
    print(f"Resto: {resto}")
